package Session3;

public class ForLoopExample {
    public static void main(String[] args) {
        System.out.println("For Loop Example");

        // print number from 0 to 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        // print number from 10 to 0
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
